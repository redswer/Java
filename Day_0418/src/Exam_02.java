
public class Exam_02 {

	public static void main(String[] args) {
		int a = 55, b = 33;
		
		System.out.println(a + " >= " + b + " : " + (a >= b));
		System.out.println(a + " <= " + b + " : " + (a <= b));
		
		boolean flag = a == b;
		System.out.println(a + " == " + b + " : " + flag);
		
		flag = a != b;
		System.out.println(a + " != " + b + " : " + flag);
	}

}
