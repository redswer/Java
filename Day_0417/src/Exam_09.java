
public class Exam_09 {

	public static void main(String[] args) {
		// shift 연산자 (<<, >>)
		
		int a = 1;
		
		System.out.println(a);
		
		int b = a << 1; // => a * 2의 1승
		System.out.println(b);
		b = a << 2; // => a * 2의 2승
		System.out.println(b);
		b = a << 3;  // => a * 2의 3승
		System.out.println(b);
		b = a << 4; // => a * 2의 4승
		System.out.println(b);
		b = a << 5;
		System.out.println(b);
	}

}
