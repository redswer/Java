
public class Exam_01 {

	public static void main(String[] args) {
		// 1. '*' 기호 25개 출력
		for (int i = 1; i <= 26; i++) {
			System.out.print("*");
		}
		
		System.out.println("\n------------------------------");
		
		// 2. 1 2 ... 25까지 정수 출력
		for (int i = 1; i <= 25; i++) {
			System.out.print(i + " ");
		}
	}

}
