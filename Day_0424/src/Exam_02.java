
public class Exam_02 {

	public static void main(String[] args) {
		// 1 2 3 4 5 ... 10 출력
		
		for (int i = 1; i <= 10; i++) {
			System.out.print(i + " ");
		}
		
		System.out.println("\n----------------------------------");
		
		int i = 0;
		while(i < 10) {
			i++;
			System.out.print(i + " ");
		}
	}

}
