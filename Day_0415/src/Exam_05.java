
public class Exam_05 {

	public static void main(String[] args) {
		// 문자열과 숫자, 문자 결합
		
		System.out.println("이름 = " + "김학생");
		System.out.println("국어 = 			" + 50);
		// => "" 안의 공백만큼 춫력 시 공백 표시
		System.out.println("영어 = " + 70);
		System.out.println("수학 = " + 80);
		System.out.println("총점 = " + 50 + 70 + 80);
		System.out.println("총점 = " + (50 + 70 + 80));
	}

}
